    private void btn_encadenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_encadenarActionPerformed
        // TODO add your handling code here:
        String rug = "", lis = "", voc = "", hiber = "",ojo = "",agua = "",tierr = "";
        if (cbx_rug.isSelected()) {
            rug = "si";
        } else {
            rug = "no";
        }
        if (cbx_lis.isSelected()) {
            lis = "si";
        } else {
            lis = "no";
        }
        if (cbx_voc.isSelected()) {
            voc = "si";
        } else {
            voc = "no";
        }
        if (cbx_hiber.isSelected()) {
            hiber = "si";
        } else {
            hiber = "no";
        }
        if (cbx_ojo.isSelected()) {
            ojo = "si";
        } else {
            ojo = "no";
        }
        if (cbx_agua.isSelected()) {
            agua = "si";
        } else {
            agua = "no";
        }
        if (cbx_tierr.isSelected()) {
            tierr = "si";
        } else {
            tierr = "no";
        }
        
         System.out.println("\nPiel rugosa=" + rug + "\nPiel lisa=" + lis + "\nVocalizaciones=" + voc + 
                 "\nHibernacion="+hiber+"\nOjos grandes="+ojo+"\nReproduccion en agua="+agua+"\nReproduccion en tierra="+tierr);
        c = objbr.getFamilia(orden, rug, lis, voc, hiber, ojo, agua, tierr);
        System.out.println("Familia3=" + c);
        lbl_res.setText(c);
    }//GEN-LAST:event_btn_encadenarActionPerformed

